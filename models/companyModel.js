/* ////////////////////////////////////////////////////////////////////////////////////////////////////
REQUIRE
//////////////////////////////////////////////////////////////////////////////////////////////////// */
const mongoose = require("mongoose");
const slugify = require("slugify");
const validator = require("validator");

/* ////////////////////////////////////////////////////////////////////////////////////////////////////
COMPANY SCHEMA
//////////////////////////////////////////////////////////////////////////////////////////////////// */
const companySchema = new mongoose.Schema(
    {
        name: {
            type: String,
            required: [true, "A company must have a name."],
            unique: true,
            trim: true,
            maxlength: [40, "A company name must have less or equal then 40 characters."],
            minlength: [3, "A company name must have more or equal then 3 characters."],
        },
        slug: String,
        summary: {
            type: String,
            trim: true,
            required: [true, "A company must have a summary."]
        },
        description: {
            type: String,
            trim: true,
            required: [true, "A company must have a description."]
        },
        imageCover: {
            type: String,
            required: [true, "A company must have a cover image."]
        },
        images: [String],
        createdAt: {
            type: Date,
            default: Date.now(),
            select: false
        },
    },
    {
        toJSON: { virtuals: true },
        toObject: { virtuals: true }
    }
);

/* ////////////////////////////////////////////////////////////////////////////////////////////////////
INDEXES. 1 = ascending, -1 = descending
//////////////////////////////////////////////////////////////////////////////////////////////////// */
companySchema.index({
    slug: 1
});

/* ////////////////////////////////////////////////////////////////////////////////////////////////////
MIDDLEWARE
//////////////////////////////////////////////////////////////////////////////////////////////////// */
companySchema.pre("save", function(next) { 
    this.slug = slugify(this.name, { lower: true });
    next();
}); 

/* ////////////////////////////////////////////////////////////////////////////////////////////////////
COMPANY MODEL
//////////////////////////////////////////////////////////////////////////////////////////////////// */
const Company = mongoose.model("Company", companySchema);

/* ////////////////////////////////////////////////////////////////////////////////////////////////////
EXPORTS
//////////////////////////////////////////////////////////////////////////////////////////////////// */
module.exports = Company;